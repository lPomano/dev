/**
 * Class get the coordinates of travel.
 */
public class Point {

    private double x;
    private double y;
    /**
     * Constructor of Point.
     */
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }
    /**
     * Method return coordinate X.
     */
    public double getX() {
        return x;
    }
    /**
     * Method return coordinate Y.
     */
    public double getY() {
        return y;
    }

}
